package practice.Training_Youtube;

public class switch_case_statement {

    public static void main(String[] args) {

        int a=3;
        switch(a){
            case 1: System.out.println("a value is 1");
            break;
            case 2: System.out.println("a value is 2");
            break;
            case 3: System.out.println("a value is 3");
            break;
            case 4: System.out.println("a value is 4");
            break;
            case 5:  System.out.println("a value is 5");
            break;
            default: System.out.println("No such cases");
        }
    }
}
 // break is used to stop execution when the condition is satisfied.